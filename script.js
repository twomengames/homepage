const header = document.querySelector("[data-header]");
const langButtons = document.querySelectorAll("[data-lang]");
const savedLanguage = localStorage.getItem("twomengames-lang");

const detectLanguage = () => {
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return languages.some((language) => language?.toLowerCase().startsWith("ko")) ? "ko" : "en";
};

const setLanguage = (language, shouldSave = true) => {
  const nextLanguage = translations[language] ? language : "en";
  const copy = translations[nextLanguage];

  document.documentElement.lang = nextLanguage;
  document.title = copy.pageTitle;
  document.querySelector("meta[name='description']")?.setAttribute("content", copy.metaDescription);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (copy[key]) {
      element.textContent = copy[key];
    }
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === nextLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (shouldSave) {
    localStorage.setItem("twomengames-lang", nextLanguage);
  }
};

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage(savedLanguage || detectLanguage(), Boolean(savedLanguage));

const updateHeader = () => {
  header?.classList.toggle("is-scrolled", window.scrollY > 12);
};

window.addEventListener("scroll", updateHeader, { passive: true });
updateHeader();

const observer = new IntersectionObserver(
  (entries) => {
    for (const entry of entries) {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    }
  },
  { threshold: 0.16 }
);

document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));
